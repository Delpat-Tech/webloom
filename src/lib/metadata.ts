export function generateMetadata(pageKey: string): Metadata {
  const pageMetadata = pagesMetadata[pageKey];
  
  if (!pageMetadata) {
    // Fallback to default metadata
    return {
      title: siteMetadata.title,
      description: siteMetadata.description,
      keywords: siteMetadata.keywords,
      authors: [{ name: siteMetadata.creator }],
      creator: siteMetadata.creator,
      publisher: siteMetadata.publisher,
      robots: 'index, follow',
      openGraph: {
        title: siteMetadata.title,
        description: siteMetadata.description,
        type: 'website',
        locale: 'en_US',
        url: siteMetadata.url,
        siteName: siteMetadata.siteName,
      },
      twitter: {
        card: 'summary_large_image',
        title: siteMetadata.title,
        description: siteMetadata.description,
      },
      icons: {
        icon: [
          { url: '/favicon.ico' },
          { url: '/favicon.svg', type: 'image/svg+xml' }
        ],
        shortcut: '/favicon.ico',
        apple: '/favicon.ico',
      },
      manifest: '/site.webmanifest',
    };
  }

  return {
    title: pageMetadata.title,
    description: pageMetadata.description,
    keywords: pageMetadata.keywords,
    authors: [{ name: siteMetadata.creator }],
    creator: siteMetadata.creator,
    publisher: siteMetadata.publisher,
    robots: 'index, follow',
    openGraph: {
      title: pageMetadata.openGraph?.title || pageMetadata.title,
      description: pageMetadata.openGraph?.description || pageMetadata.description,
      type: pageMetadata.openGraph?.type || 'website',
      locale: 'en_US',
      url: pageMetadata.openGraph?.url || `${siteMetadata.url}/${pageKey}`,
      siteName: siteMetadata.siteName,
      images: pageMetadata.openGraph?.image ? [{ url: pageMetadata.openGraph.image }] : undefined,
    },
    twitter: {
      card: pageMetadata.twitter?.card || 'summary_large_image',
      title: pageMetadata.twitter?.title || pageMetadata.title,
      description: pageMetadata.twitter?.description || pageMetadata.description,
      images: pageMetadata.twitter?.image ? [pageMetadata.twitter.image] : undefined,
    },
    icons: {
      icon: [
        { url: '/favicon.ico' },
        { url: '/favicon.svg', type: 'image/svg+xml' }
      ],
      shortcut: '/favicon.ico',
      apple: '/favicon.ico',
    },
    manifest: '/site.webmanifest',
  };
}

